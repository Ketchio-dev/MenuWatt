enum SpriteRenderer {
    static func image(for frame: SpriteFrame) -> NSImage {
        let pixels = frame.pixels
        let imageSize = NSSize(width: 18, height: 18)
        let cellSize: CGFloat = 1
        let pixelWidth = CGFloat(pixels.first?.count ?? 0) * cellSize
        let pixelHeight = CGFloat(pixels.count) * cellSize
        let originX = floor((imageSize.width - pixelWidth) / 2)
        let originY = floor((imageSize.height - pixelHeight) / 2)

        let image = NSImage(size: imageSize)
        image.lockFocus()

        if let context = NSGraphicsContext.current?.cgContext {
            context.setShouldAntialias(false)
            context.interpolationQuality = .none
        }

        NSColor.black.setFill()

        for (rowIndex, row) in pixels.enumerated() {
            for (columnIndex, character) in row.enumerated() where character == "#" {
                let rect = NSRect(
                    x: originX + CGFloat(columnIndex) * cellSize,
                    y: originY + CGFloat(pixels.count - rowIndex - 1) * cellSize,
                    width: cellSize,
                    height: cellSize
                )
                NSBezierPath(rect: rect).fill()
            }
        }

        image.unlockFocus()
        image.isTemplate = true
        return image
    }
}
